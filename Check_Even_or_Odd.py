# Step 1: Take an integer input from the user
number = int(input("Enter an integer: "))

# Step 2: Check if the number is divisible by 2
if number % 2 == 0:
    print(f"The number {number} is Even.")
else:
    print(f"The number {number} is Odd.")
# Step 3: End of the program
# The program will output whether the number is even or odd based on the condition checked.