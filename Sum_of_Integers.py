# Step 1: Initialize a variable to store the sum
sum_total = 0

# Step 2: Loop through numbers 1 to 50
for i in range(1, 51):
    sum_total += i  # Add each number to the sum

# Step 3: Print the final result
print(f"\nThe sum of integers from 1 to 50 is: {sum_total}")
# Step 4: End of the program
# The program calculates the sum of integers from 1 to 50 and prints the result.